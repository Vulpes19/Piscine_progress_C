int	ft_sqrt(int nb)
{
	int	sqt;

	sqt = 3;
	if (nb == 1)
		return (1);
	if (nb == 4)
		return (2);
	if (nb == 9)
		return (3);
	while (sqt < nb && sqt <= 46340)
	{
		if (sqt * sqt == nb)
			return (sqt);
		sqt++;
	}	
	return (0);
}
