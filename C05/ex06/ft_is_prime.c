int	ft_is_prime(int nb)
{
	int	pr;

	pr = 2;
	if (nb < 0)
		return (0);
	if (nb == 1 || nb == 0)
		return (0);
	while (pr < nb)
	{
		if (nb % pr == 0)
			return (0);
		pr++;
	}
	return (1);
}
