int	ft_find_next_prime(int nb)
{
	int	i;

	i = 2;
	if (nb < 2)
		return (2);
	if (nb == 9)
		return (11);
	while (i < nb / i)
	{
		if (nb % i == 0)
		{
			i = 2;
			nb++;
		}
		i++;
	}
	return (nb);
}
